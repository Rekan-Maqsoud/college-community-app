const STAGE_KEYS = ['firstYear', 'secondYear', 'thirdYear', 'fourthYear', 'fifthYear', 'sixthYear'];

const normalizeDepartmentEntry = (departmentEntry) => {
  if (typeof departmentEntry === 'string') {
    return { key: departmentEntry, years: 4 };
  }

  if (!departmentEntry || typeof departmentEntry !== 'object') {
    return null;
  }

  const safeYears = Number.isInteger(departmentEntry.years)
    ? departmentEntry.years
    : 4;

  return {
    key: departmentEntry.key,
    years: Math.min(6, Math.max(1, safeYears)),
  };
};

const getNormalizedDepartmentEntries = (universityKey, collegeKey) => {
  if (!universityKey || !collegeKey || !universitiesData[universityKey]) {
    return [];
  }

  const university = universitiesData[universityKey];
  if (!university.colleges[collegeKey]) {
    return [];
  }

  return university.colleges[collegeKey].departments
    .map(normalizeDepartmentEntry)
    .filter(Boolean);
};

export const getUniversityKeys = () => {
  return Object.keys(universitiesData);
};

export const getCollegesForUniversity = (universityKey) => {
  if (!universityKey || !universitiesData[universityKey]) {
    return [];
  }
  return Object.keys(universitiesData[universityKey].colleges);
};

export const getDepartmentsForCollege = (universityKey, collegeKey) => {
  return getNormalizedDepartmentEntries(universityKey, collegeKey).map((department) => department.key);
};

export const getStagesForDepartment = (universityKey, collegeKey, departmentKey) => {
  if (!departmentKey) {
    return STAGE_KEYS.slice(0, 4);
  }

  const department = getNormalizedDepartmentEntries(universityKey, collegeKey)
    .find((entry) => entry.key === departmentKey);

  const years = department?.years || 4;
  return STAGE_KEYS.slice(0, years);
};
